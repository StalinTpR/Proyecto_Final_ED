    public void MedicamentosCarga(Medicamento m, Medicamento[] s) {
        Medicamento[] medicamento = new Medicamento[s.length];
        int cont = 0;
        for (int i = 0; i < medicamento.length; i++) {
            if (cont == i && i + 1 < medicamento.length) {
                if (s.length == 0) {
                    medicamento[i] = m;
                    break;
                }
                medicamento[i] = s[i];
                cont++;
            } else {
                medicamento[i] = m;
            }
        }
        medicamentos = medicamento;
    }

